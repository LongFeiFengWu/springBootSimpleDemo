package demo.module;

import demo.pojo.Role;
import demo.pojo.User;

public class LoginInfo {

	private User user;
	private Role role;
	
	private  String token;

	public User getUser() {
		return user;
	}

	public void setUser(User user) {
		this.user = user;
	}

	public Role getRole() {
		return role;
	}

	public void setRole(Role role) {
		this.role = role;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}

}
